#include<bits/stdc++.h>

using namespace std;

int main(){
    int bx,by,mx,my;
    int map[30][30]={0};
    long long step[30][30]={0};
    const int fx[] = { -2, -1, 1, 2, 2, 1, -1, -2};
    const int fy[] = { 1, 2, 2, 1, -1, -2, -2, -1};
    cin>>bx>>by>>mx>>my;
    bx+=1;
    by+=1;
    mx+=1;
    my+=1;
    map[mx][my]=1;
    for(int i=0;i<8;i++){
        map[mx+fx[i]][my+fy[i]]=1;
    }
    //初始化
    step[0][1]=1;
    for(int i=1;i<=bx;i++){
        for(int j=1;j<=by;j++){
            if(map[i][j]==1){
                continue;
            }
            step[i][j]=step[i-1][j]+step[i][j-1];
        }
    }
    cout<<step[bx][by]<<endl;
    return 0;

}