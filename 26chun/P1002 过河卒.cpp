// 这道题初始位置是从 0 开始的，这样不是很利于我们解题，所以不如暂且把这题里涉及的坐标统统 +1，那么初始位置就从 (0,0) 变成了 (1,1)。

// 先考虑如果没有任何马的限制，卒子可以随便向右向下走，那么可以想到，一个卒子只能从 当前格子的左侧格子 和 当前格子的上方格子 上走到当前格子。
// 那么假设从 (1,1) 走到 当前格子的左侧格子 的路径条数是 x，从 (1,1) 走到 当前格子的上方格子 的路径条数是 y，那么从 (1,1) 走到当前格子的路径条数就应该是 x+y。

// 其实我们已经得到了一个动态规划的转移方程，设 f(i,j) 表示从 (1,1) 格子走到当前格子的路径条数，那么根据上一段得到的结论，可以得到：

// f(i,j)=f(i−1,j)+f(i,j−1)
// (i,j) 是当前格子，那么 (i−1,j) 就是 当前格子的上方格子，(i,j−1) 就是 当前格子的左侧格子。
// 我们只需要从小到大依次枚举 i 和 j 就能获得所有点的答案，可以想到，在这道题里我们要求的答案就是 f(n,m)（因为 B 点的坐标是(n,m)）。

// 当然如果只是按照这个公式推肯定不行，因为 f 的初始数值都是 0，再怎么推也都是 0，我们要让 f(1,1) 能根据上面得到的式子推出答案是 1，这样才能有有意义的结果。
// 根据 f(1,1)=f(0,1)+f(1,0)，我们只需要让 f(1,0)=1 或者 f(0,1)=1 即可。

// 接下来考虑一下加入了 马 这道题该怎么做，假设 (x,y) 这个点被马拦住了，其实就是说这个点不能被卒子走到，那当我们枚举到这个点的时候，发现他被马拦住了，那就直接跳过这个点，让 f(x,y)=0 就行了。

// 具体写代码的时候我们注意到在判断一个点有没有被马拦住时，会用到 (i−2,j−1) 和 (i−1,j−2) 这两个位置，那如果不把所有的点的坐标都加上 2 （前面分析的时候只把所有的坐标加上 1），就会因为数组越界而 WA 掉一个点。

